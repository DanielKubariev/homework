//	Определить какой четверти принадлежит точка 
// с координатами (х,у)
var x = 0;
var y = 1;
if (x > 0 && y > 0) {
    console.log('Первая четверть');
} else if (x < 0 && y > 0) {
    console.log('Вторая четверть');
} else if (x < 0 && y < 0) {
    console.log('Третья четверть');
} else if (x > 0 && y < 0) {
    console.log('Четвертая четверть');
} else { 
    console.log('Не в четверти');
}
